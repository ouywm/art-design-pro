export const normalizeReleaseDetail = (detail: string): NormalizedReleaseDetail => {
  const matched = detail.match(prefixPattern)
  const tag = (matched?.[1] ?? '新增') as ReleaseTag
  const text = detail.replace(prefixPattern, '').trim()

  return {
    tag,
    type: tagTypeMap[tag],
    text
  }
}

export const buildReleaseStats = (releases: ReleaseLogItem[]): ReleaseStats => {
  const currentVersion = releases[0]?.version ?? '-'
  const detailCount = releases.reduce((total, item) => total + item.details.length, 0)

  return {
    currentVersion,
    releaseCount: releases.length,
    detailCount
  }
}

export const toReleaseId = (item: Pick<ReleaseLogItem, 'version' | 'date'>) => {
  return `release-${item.version}-${item.date}`
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
}
